import {Assert} from '../../../../../suite/assert.js'

function getProperty<T, K extends keyof T>(obj: T, key: K) {
  return obj[key];
}
let x = { a: 1, b: 2, c: 3, d: 4 };
Assert.equal(getProperty(x, 'a'), 1);