/**---
 description: >
  A readonly tuple will now treat its length property as readonly. 
 ---*/


function overwriteLength(tuple: readonly [string, string, string]) {
    return tuple.length
}
var a = overwriteLength(['a', 'b', 'c'])
Assert.equal(a, 3)